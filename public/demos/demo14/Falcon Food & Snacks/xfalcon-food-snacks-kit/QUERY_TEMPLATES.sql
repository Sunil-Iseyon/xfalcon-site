-- Rows: ~6-8 (market tiers)
SELECT
    g.MARKET_TIER,
    COUNT(DISTINCT g.STATE_PROVINCE) as STATE_COUNT,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    ROUND(AVG(f.NET_REVENUE), 0) as AVG_TRANSACTION_SIZE,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT
FROM FACT_POS_SALES f
JOIN DIM_GEOGRAPHY g ON f.GEO_KEY = g.GEO_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY g.MARKET_TIER
ORDER BY NET_REVENUE DESC;

-- Query 11c: Geographic growth YoY by region
-- Rows: ~12-16 (regions × 2 years)
SELECT
    d.YEAR_NUM,
    g.REGION,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    COUNT(DISTINCT f.CUSTOMER_KEY) as CUSTOMER_COUNT,
    LAG(SUM(f.NET_REVENUE)) OVER (PARTITION BY g.REGION ORDER BY d.YEAR_NUM) as PRIOR_YEAR_REVENUE
FROM FACT_POS_SALES f
JOIN DIM_GEOGRAPHY g ON f.GEO_KEY = g.GEO_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, g.REGION
ORDER BY d.YEAR_NUM DESC, NET_REVENUE DESC;

-- Query 11d: US vs Canada performance comparison
-- Rows: ~24 (12 months × 2 countries)
SELECT
    d.MONTH_NUM,
    d.MONTH_NAME,
    d.YEAR_NUM,
    CASE WHEN g.COUNTRY_CODE = 'US' THEN 'United States' ELSE 'Canada' END as COUNTRY,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    COUNT(DISTINCT f.CUSTOMER_KEY) as CUSTOMERS,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT
FROM FACT_POS_SALES f
JOIN DIM_GEOGRAPHY g ON f.GEO_KEY = g.GEO_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.MONTH_NUM, d.MONTH_NAME, d.YEAR_NUM, CASE WHEN g.COUNTRY_CODE = 'US' THEN 'United States' ELSE 'Canada' END
ORDER BY d.YEAR_NUM DESC, d.MONTH_NUM, COUNTRY;

