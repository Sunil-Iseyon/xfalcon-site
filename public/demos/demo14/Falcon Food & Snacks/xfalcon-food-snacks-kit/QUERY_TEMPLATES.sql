-- 10. CONSUMER PANEL / HOUSEHOLD
-- ============================================================
-- KPIs: Household penetration, repeat rate, loyalty tier, basket size, purchase frequency

-- Query 10a: Household penetration and repeat purchase analysis
-- Rows: ~5-6 (loyalty tiers)
SELECT
    h.LOYALTY_TIER,
    COUNT(DISTINCT h.HOUSEHOLD_KEY) as HOUSEHOLD_COUNT,
    SUM(CASE WHEN f.IS_REPEAT_PURCHASE = 1 THEN 1 ELSE 0 END) as REPEAT_PURCHASES,
    ROUND(100.0 * SUM(CASE WHEN f.IS_REPEAT_PURCHASE = 1 THEN 1 ELSE 0 END) / NULLIF(COUNT(*), 0), 1) as REPEAT_RATE_PCT,
    ROUND(AVG(f.AMOUNT_PAID), 2) as AVG_BASKET_SIZE,
    ROUND(AVG(f.UNITS_PURCHASED), 1) as AVG_UNITS_PER_TRIP,
    ROUND(COUNT(DISTINCT f.DATE_KEY) / COUNT(DISTINCT h.HOUSEHOLD_KEY), 1) as AVG_TRIPS_PER_HH
