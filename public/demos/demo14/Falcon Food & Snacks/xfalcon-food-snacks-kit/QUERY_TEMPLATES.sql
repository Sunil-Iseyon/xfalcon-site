GROUP BY ch.CHANNEL_NAME, p.BRAND
ORDER BY NET_REVENUE DESC
LIMIT 80;

-- ============================================================
-- 7. SHIPMENTS & FILL RATE
-- ============================================================
-- KPIs: Monthly shipments, fill rate trend, on-time %, supplier performance

-- Query 7a: Monthly shipment volume and fill rate
-- Rows: ~24 (12 months × 2 years)
SELECT
    d.YEAR_NUM,
    d.MONTH_NUM,
    d.MONTH_NAME,
    SUM(f.CASES_SHIPPED) as TOTAL_CASES_SHIPPED,
    SUM(f.UNITS_SHIPPED) as TOTAL_UNITS_SHIPPED,
    SUM(f.NET_INVOICE_VALUE) as INVOICE_VALUE,
    ROUND(100.0 * SUM(f.CASES_SHIPPED) / NULLIF(SUM(f.CASES_SHIPPED) + NULLIF(SUM(CASE WHEN f.FILL_RATE_PCT < 100 THEN f.CASES_SHIPPED ELSE 0 END), 0), 0), 1) as FILL_RATE_PCT,
    COUNT(DISTINCT f.DATE_KEY) as SHIPPING_DAYS
FROM FACT_SHIPMENTS f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, d.MONTH_NUM, d.MONTH_NAME
ORDER BY d.YEAR_NUM DESC, d.MONTH_NUM;

-- Query 7b: On-time delivery performance by supplier
-- Rows: ~15-20 (top suppliers)
SELECT
    s.SUPPLIER_NAME,
    s.SUPPLIER_TYPE,
    s.COUNTRY_OF_ORIGIN,
    COUNT(DISTINCT f.SHIPMENT_KEY) as TOTAL_SHIPMENTS,
    SUM(f.UNITS_SHIPPED) as UNITS_SHIPPED,
    SUM(CASE WHEN f.ON_TIME_FLAG = 1 THEN 1 ELSE 0 END) as ON_TIME_COUNT,
    ROUND(100.0 * SUM(CASE WHEN f.ON_TIME_FLAG = 1 THEN 1 ELSE 0 END) / NULLIF(COUNT(DISTINCT f.SHIPMENT_KEY), 0), 1) as ON_TIME_PCT,
    ROUND(AVG(f.FILL_RATE_PCT), 1) as AVG_FILL_RATE_PCT
FROM FACT_SHIPMENTS f
JOIN DIM_SUPPLIER s ON f.SUPPLIER_KEY = s.SUPPLIER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY s.SUPPLIER_NAME, s.SUPPLIER_TYPE, s.COUNTRY_OF_ORIGIN
ORDER BY UNITS_SHIPPED DESC
LIMIT 20;

-- Query 7c: Customer fill rate performance
-- Rows: ~15-20 (top customers)
SELECT
    c.CUSTOMER_NAME,
    c.CUSTOMER_TIER,
    c.CHANNEL,
    COUNT(DISTINCT f.SHIPMENT_KEY) as SHIPMENTS,
    SUM(f.UNITS_SHIPPED) as UNITS_SHIPPED,
    ROUND(AVG(f.FILL_RATE_PCT), 1) as AVG_FILL_RATE_PCT,
    SUM(CASE WHEN f.ON_TIME_FLAG = 1 THEN 1 ELSE 0 END) as ON_TIME_SHIPMENTS,
    ROUND(100.0 * SUM(CASE WHEN f.ON_TIME_FLAG = 1 THEN 1 ELSE 0 END) / NULLIF(COUNT(DISTINCT f.SHIPMENT_KEY), 0), 1) as ON_TIME_PCT
FROM FACT_SHIPMENTS f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_NAME, c.CUSTOMER_TIER, c.CHANNEL
ORDER BY UNITS_SHIPPED DESC
LIMIT 20;

-- Query 7d: Fill rate trend by product category
-- Rows: ~12-16 (categories × 2 years)
SELECT
    d.YEAR_NUM,
    p.CATEGORY,
    COUNT(DISTINCT f.SHIPMENT_KEY) as SHIPMENTS,
    ROUND(AVG(f.FILL_RATE_PCT), 1) as AVG_FILL_RATE_PCT,
    ROUND(100.0 * SUM(CASE WHEN f.ON_TIME_FLAG = 1 THEN 1 ELSE 0 END) / NULLIF(COUNT(DISTINCT f.SHIPMENT_KEY), 0), 1) as ON_TIME_PCT
FROM FACT_SHIPMENTS f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, p.CATEGORY
ORDER BY d.YEAR_NUM DESC, CATEGORY;

-- ============================================================
-- 8. INVENTORY & OPERATIONS
-- ============================================================
-- KPIs: WOS trend, OOS rate, overstock rate, inventory by customer/product

-- Query 8a: Weeks of supply trend by month
-- Rows: ~24 (12 months × 2 years)
SELECT
    d.YEAR_NUM,
    d.MONTH_NUM,
    d.MONTH_NAME,
    ROUND(AVG(f.WEEKS_OF_SUPPLY), 2) as AVG_WOS,
    ROUND(PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY f.WEEKS_OF_SUPPLY), 2) as MEDIAN_WOS,
    ROUND(MAX(f.WEEKS_OF_SUPPLY), 2) as MAX_WOS,
    ROUND(MIN(f.WEEKS_OF_SUPPLY), 2) as MIN_WOS,
    COUNT(DISTINCT CASE WHEN f.OUT_OF_STOCK_FLAG = 1 THEN f.INVENTORY_KEY ELSE NULL END) as OOS_POSITIONS,
    COUNT(DISTINCT CASE WHEN f.OVERSTOCK_FLAG = 1 THEN f.INVENTORY_KEY ELSE NULL END) as OVERSTOCK_POSITIONS
FROM FACT_INVENTORY f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, d.MONTH_NUM, d.MONTH_NAME
ORDER BY d.YEAR_NUM DESC, d.MONTH_NUM;

-- Query 8b: Out-of-stock and overstock alerts
-- Rows: ~50-100 (product-customer combinations with issues)
SELECT
    d.FULL_DATE,
    p.PRODUCT_NAME,
    p.SKU_CODE,
    p.CATEGORY,
    c.CUSTOMER_NAME,
    c.CHANNEL,
    f.ON_HAND_CASES,
    f.ON_ORDER_CASES,
    f.IN_TRANSIT_CASES,
    ROUND(f.WEEKS_OF_SUPPLY, 1) as WEEKS_OF_SUPPLY,
    CASE WHEN f.OUT_OF_STOCK_FLAG = 1 THEN 'OUT OF STOCK'
         WHEN f.OVERSTOCK_FLAG = 1 THEN 'OVERSTOCK'
         WHEN f.WEEKS_OF_SUPPLY < 1 THEN 'LOW STOCK'
         ELSE 'NORMAL' END as INVENTORY_STATUS
FROM FACT_INVENTORY f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
WHERE d.FULL_DATE = (SELECT MAX(FULL_DATE) FROM DIM_DATE)
  AND (f.OUT_OF_STOCK_FLAG = 1 OR f.OVERSTOCK_FLAG = 1 OR f.WEEKS_OF_SUPPLY < 1)
ORDER BY CASE WHEN f.OUT_OF_STOCK_FLAG = 1 THEN 1 WHEN f.WEEKS_OF_SUPPLY < 1 THEN 2 ELSE 3 END,
         f.WEEKS_OF_SUPPLY ASC
LIMIT 100;

-- Query 8c: Inventory value by customer and product tier
-- Rows: ~30-40 (customers × product tiers)
SELECT
    c.CUSTOMER_NAME,
    c.CUSTOMER_TIER,
    p.CATEGORY,
    p.BRAND_TIER,
    SUM(f.ON_HAND_CASES) as ON_HAND_CASES,
    SUM(f.ON_ORDER_CASES) as ON_ORDER_CASES,
    ROUND(AVG(f.WEEKS_OF_SUPPLY), 1) as AVG_WOS,
    COUNT(DISTINCT f.INVENTORY_KEY) as SKU_COUNT
FROM FACT_INVENTORY f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FULL_DATE = (SELECT MAX(FULL_DATE) FROM DIM_DATE)
GROUP BY c.CUSTOMER_NAME, c.CUSTOMER_TIER, p.CATEGORY, p.BRAND_TIER
ORDER BY ON_HAND_CASES DESC
LIMIT 40;

-- ============================================================
-- 9. TRADE SPEND / PROMO ROI
-- ============================================================
-- KPIs: Spend planned vs actual, ROI by brand/customer, incremental lift, promo effectiveness

-- Query 9a: Trade spend planned vs actual by brand
-- Rows: ~10-15 (top brands)
SELECT
    p.BRAND,
    SUM(f.PLANNED_SPEND) as PLANNED_SPEND,
    SUM(f.ACTUAL_SPEND) as ACTUAL_SPEND,
    ROUND(100.0 * (SUM(f.ACTUAL_SPEND) - SUM(f.PLANNED_SPEND)) / NULLIF(SUM(f.PLANNED_SPEND), 0), 1) as VARIANCE_PCT,
    SUM(f.BASELINE_UNITS) as BASELINE_UNITS,
    SUM(f.INCREMENTAL_UNITS) as INCREMENTAL_UNITS,
    ROUND(100.0 * SUM(f.INCREMENTAL_UNITS) / NULLIF(SUM(f.BASELINE_UNITS), 0), 1) as LIFT_PCT,
    ROUND(SUM(f.PROMO_ROI), 2) as AVG_ROI
FROM FACT_TRADE_SPEND f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY p.BRAND
ORDER BY SUM(f.ACTUAL_SPEND) DESC
LIMIT 15;

-- Query 9b: Promo ROI by promotion type
-- Rows: ~5-8 (promotion types)
SELECT
    pr.PROMOTION_TYPE,
    pr.DISCOUNT_TYPE,
    COUNT(DISTINCT f.TRADE_SPEND_KEY) as PROMO_COUNT,
    SUM(f.ACTUAL_SPEND) as TOTAL_SPEND,
    SUM(f.BASELINE_REVENUE) as BASELINE_REVENUE,
    SUM(f.INCREMENTAL_REVENUE) as INCREMENTAL_REVENUE,
    ROUND(SUM(f.INCREMENTAL_REVENUE) / NULLIF(SUM(f.ACTUAL_SPEND), 0), 2) as ROI,
    ROUND(100.0 * SUM(f.INCREMENTAL_UNITS) / NULLIF(SUM(f.BASELINE_UNITS), 0), 1) as LIFT_PCT
FROM FACT_TRADE_SPEND f
JOIN DIM_PROMOTION pr ON f.PROMOTION_KEY = pr.PROMOTION_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE) AND f.ACTUAL_SPEND > 0
GROUP BY pr.PROMOTION_TYPE, pr.DISCOUNT_TYPE
ORDER BY SUM(f.ACTUAL_SPEND) DESC;

-- Query 9c: Customer trade spend effectiveness
-- Rows: ~15-20 (top customers)
SELECT
    c.CUSTOMER_NAME,
    c.CUSTOMER_TIER,
    SUM(f.ACTUAL_SPEND) as TOTAL_SPEND,
    COUNT(DISTINCT f.TRADE_SPEND_KEY) as PROMO_COUNT,
    SUM(f.INCREMENTAL_REVENUE) as INCREMENTAL_REVENUE,
    SUM(f.INCREMENTAL_UNITS) as INCREMENTAL_UNITS,
    ROUND(SUM(f.INCREMENTAL_REVENUE) / NULLIF(SUM(f.ACTUAL_SPEND), 0), 2) as ROI,
    ROUND(100.0 * SUM(f.INCREMENTAL_UNITS) / NULLIF(SUM(f.BASELINE_UNITS), 0), 1) as LIFT_PCT
FROM FACT_TRADE_SPEND f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_NAME, c.CUSTOMER_TIER
ORDER BY SUM(f.ACTUAL_SPEND) DESC
LIMIT 20;

-- Query 9d: Incremental lift by category
-- Rows: ~8-12 (product categories)
SELECT
    p.CATEGORY,
    SUM(f.BASELINE_UNITS) as BASELINE_UNITS,
    SUM(f.INCREMENTAL_UNITS) as INCREMENTAL_UNITS,
    SUM(f.BASELINE_REVENUE) as BASELINE_REVENUE,
    SUM(f.INCREMENTAL_REVENUE) as INCREMENTAL_REVENUE,
    SUM(f.ACTUAL_SPEND) as PROMO_SPEND,
    ROUND(100.0 * SUM(f.INCREMENTAL_UNITS) / NULLIF(SUM(f.BASELINE_UNITS), 0), 1) as UNIT_LIFT_PCT,
    ROUND(100.0 * SUM(f.INCREMENTAL_REVENUE) / NULLIF(SUM(f.BASELINE_REVENUE), 0), 1) as REVENUE_LIFT_PCT,
    ROUND(SUM(f.INCREMENTAL_REVENUE) / NULLIF(SUM(f.ACTUAL_SPEND), 0), 2) as ROI
FROM FACT_TRADE_SPEND f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY p.CATEGORY
ORDER BY SUM(f.ACTUAL_SPEND) DESC;

