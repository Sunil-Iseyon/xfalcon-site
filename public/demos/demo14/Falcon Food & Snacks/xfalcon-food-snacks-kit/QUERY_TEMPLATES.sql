-- ============================================================
-- 1. EXECUTIVE OVERVIEW
-- ============================================================
-- KPIs: Net revenue trend YoY, gross margin %, top 5 brands, top 5 customers

-- Query 1a: Monthly net revenue YoY comparison with margin
-- Rows: ~24 (2 years × 12 months)
SELECT
    d.YEAR_NUM,
    d.MONTH_NUM,
    d.MONTH_NAME,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.COGS) as COGS,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT,
    COUNT(DISTINCT f.DATE_KEY) as TRADING_DAYS
FROM FACT_POS_SALES f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, d.MONTH_NUM, d.MONTH_NAME
ORDER BY d.YEAR_NUM DESC, d.MONTH_NUM;

-- Query 1b: Top 5 brands by net revenue (YTD)
-- Rows: 5
SELECT
    p.BRAND,
    p.BRAND_TIER,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY p.BRAND, p.BRAND_TIER
ORDER BY NET_REVENUE DESC
LIMIT 5;

-- Query 1c: Top 5 customers by net revenue (YTD)
-- Rows: 5
SELECT
    c.CUSTOMER_NAME,
    c.CUSTOMER_TIER,
    c.CHANNEL,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_NAME, c.CUSTOMER_TIER, c.CHANNEL
ORDER BY NET_REVENUE DESC
LIMIT 5;

-- Query 1d: Quarterly performance snapshot
-- Rows: ~8 (2 years × 4 quarters)
SELECT
    d.YEAR_NUM,
    d.FISCAL_QUARTER,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.GROSS_PROFIT) as GROSS_PROFIT,
    ROUND(100.0 * SUM(f.GROSS_PROFIT) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT,
    SUM(f.CASES_SOLD) as CASES_SOLD
FROM FACT_POS_SALES f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, d.FISCAL_QUARTER
ORDER BY d.YEAR_NUM DESC, d.FISCAL_QUARTER;

-- ============================================================
-- 2. SALES & REVENUE
-- ============================================================
-- KPIs: Monthly trend, channel mix, brand breakdown, customer ranking

-- Query 2a: Monthly revenue by channel with trend
-- Rows: ~24 (12 months × 2 channels avg)
SELECT
    d.MONTH_NUM,
    d.MONTH_NAME,
    d.YEAR_NUM,
    ch.CHANNEL_NAME,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    SUM(f.CASES_SOLD) as CASES_SOLD,
    COUNT(DISTINCT f.CUSTOMER_KEY) as UNIQUE_CUSTOMERS
FROM FACT_POS_SALES f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
JOIN DIM_CHANNEL ch ON f.CHANNEL_KEY = ch.CHANNEL_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.MONTH_NUM, d.MONTH_NAME, d.YEAR_NUM, ch.CHANNEL_NAME
ORDER BY d.YEAR_NUM DESC, d.MONTH_NUM, ch.CHANNEL_NAME;

-- Query 2b: Channel revenue mix (current month vs prior year)
-- Rows: ~6-8 (2 years × channels)
SELECT
    d.YEAR_NUM,
    ch.CHANNEL_NAME,
    ch.CHANNEL_GROUP,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    ROUND(100.0 * SUM(f.NET_REVENUE) / SUM(SUM(f.NET_REVENUE)) OVER (PARTITION BY d.YEAR_NUM), 1) as PCT_OF_TOTAL
FROM FACT_POS_SALES f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
JOIN DIM_CHANNEL ch ON f.CHANNEL_KEY = ch.CHANNEL_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, ch.CHANNEL_NAME, ch.CHANNEL_GROUP
ORDER BY d.YEAR_NUM DESC, NET_REVENUE DESC;

-- Query 2c: Brand revenue ranking with YoY comparison
-- Rows: ~15-20 (top brands)
SELECT
    p.BRAND,
    SUM(CASE WHEN d.YEAR_NUM = YEAR(CURRENT_DATE) THEN f.NET_REVENUE ELSE 0 END) as CURRENT_YEAR_REVENUE,
    SUM(CASE WHEN d.YEAR_NUM = YEAR(CURRENT_DATE) - 1 THEN f.NET_REVENUE ELSE 0 END) as PRIOR_YEAR_REVENUE,
    ROUND(100.0 * (SUM(CASE WHEN d.YEAR_NUM = YEAR(CURRENT_DATE) THEN f.NET_REVENUE ELSE 0 END) -
           SUM(CASE WHEN d.YEAR_NUM = YEAR(CURRENT_DATE) - 1 THEN f.NET_REVENUE ELSE 0 END)) /
           NULLIF(SUM(CASE WHEN d.YEAR_NUM = YEAR(CURRENT_DATE) - 1 THEN f.NET_REVENUE ELSE 0 END), 0), 1) as YOY_GROWTH_PCT
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY p.BRAND
ORDER BY CURRENT_YEAR_REVENUE DESC
LIMIT 15;

-- Query 2d: Customer tier revenue distribution
-- Rows: ~4-5 (customer tiers)
SELECT
    c.CUSTOMER_TIER,
    COUNT(DISTINCT c.CUSTOMER_KEY) as CUSTOMER_COUNT,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    ROUND(AVG(f.NET_REVENUE), 0) as AVG_TRANSACTION_VALUE
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_TIER
ORDER BY NET_REVENUE DESC;

-- ============================================================
-- 3. PRODUCT PERFORMANCE
-- ============================================================
-- KPIs: Brand/category revenue, units, margin; top/bottom SKUs; new product tracking

-- Query 3a: Revenue and units by brand and category
-- Rows: ~30-40 (brands × categories)
SELECT
    p.BRAND,
    p.CATEGORY,
    p.SUB_CATEGORY,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    ROUND(AVG(f.BASE_PRICE), 2) as AVG_PRICE,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY p.BRAND, p.CATEGORY, p.SUB_CATEGORY
ORDER BY NET_REVENUE DESC
LIMIT 40;

-- Query 3b: Top 10 SKUs by revenue
-- Rows: 10
SELECT
    p.SKU_CODE,
    p.PRODUCT_NAME,
    p.CATEGORY,
    p.PACK_SIZE,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT,
    COUNT(DISTINCT f.CUSTOMER_KEY) as CUSTOMERS_CARRYING
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE) AND p.IS_ACTIVE = 1
GROUP BY p.SKU_CODE, p.PRODUCT_NAME, p.CATEGORY, p.PACK_SIZE
ORDER BY NET_REVENUE DESC
LIMIT 10;

-- Query 3c: Bottom 10 SKUs (performance concern flag)
-- Rows: 10
SELECT
    p.SKU_CODE,
    p.PRODUCT_NAME,
    p.CATEGORY,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    COUNT(DISTINCT f.CUSTOMER_KEY) as CUSTOMERS_CARRYING,
    CASE WHEN COUNT(DISTINCT f.CUSTOMER_KEY) < 5 THEN 'HIGH RISK - Limited Distribution'
         WHEN SUM(f.NET_REVENUE) < 10000 THEN 'REVIEW - Low Revenue'
         ELSE 'MONITOR' END as ACTION_FLAG
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE) AND p.IS_ACTIVE = 1
GROUP BY p.SKU_CODE, p.PRODUCT_NAME, p.CATEGORY
HAVING SUM(f.NET_REVENUE) > 0
ORDER BY NET_REVENUE ASC
LIMIT 10;

-- Query 3d: New and seasonal product tracking
-- Rows: ~15-20 (new/seasonal items)
SELECT
    p.PRODUCT_NAME,
    p.SKU_CODE,
    p.BRAND,
    p.IS_SEASONAL,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    COUNT(DISTINCT f.CUSTOMER_KEY) as CUSTOMERS_CARRYING,
    COUNT(DISTINCT f.DATE_KEY) as DAYS_WITH_SALES
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE) AND (p.IS_SEASONAL = 1 OR p.IS_ACTIVE = 1)
GROUP BY p.PRODUCT_NAME, p.SKU_CODE, p.BRAND, p.IS_SEASONAL
HAVING COUNT(DISTINCT f.DATE_KEY) < 90  -- Products active <90 days = newer items
ORDER BY DAYS_WITH_SALES DESC
LIMIT 20;

-- ============================================================
-- 4. PRODUCT MARGIN
-- ============================================================
-- KPIs: Margin % by brand, category, customer; COGS trend; trade discount impact

-- Query 4a: Gross margin % by brand and category
-- Rows: ~30-40 (brand × category combinations)
SELECT
    p.BRAND,
    p.CATEGORY,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.COGS) as COGS,
    SUM(f.GROSS_PROFIT) as GROSS_PROFIT,
    ROUND(100.0 * SUM(f.GROSS_PROFIT) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT,
    COUNT(DISTINCT f.PRODUCT_KEY) as SKU_COUNT
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY p.BRAND, p.CATEGORY
ORDER BY GROSS_MARGIN_PCT DESC;

-- Query 4b: Margin % by customer tier
-- Rows: ~4-5 (customer tiers)
SELECT
    c.CUSTOMER_TIER,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.COGS) as COGS,
    SUM(f.GROSS_PROFIT) as GROSS_PROFIT,
    SUM(f.TRADE_DISCOUNT) as TOTAL_TRADE_DISCOUNT,
    ROUND(100.0 * SUM(f.GROSS_PROFIT) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT,
    ROUND(100.0 * SUM(f.TRADE_DISCOUNT) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as TRADE_DISCOUNT_PCT
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_TIER
ORDER BY GROSS_MARGIN_PCT DESC;

-- Query 4c: COGS trend by month (YoY)
-- Rows: ~24 (2 years × 12 months)
SELECT
    d.YEAR_NUM,
    d.MONTH_NUM,
    d.MONTH_NAME,
    SUM(f.COGS) as TOTAL_COGS,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    ROUND(100.0 * SUM(f.COGS) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as COGS_AS_PCT_REVENUE
FROM FACT_POS_SALES f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.YEAR_NUM, d.MONTH_NUM, d.MONTH_NAME
ORDER BY d.YEAR_NUM DESC, d.MONTH_NUM;

-- Query 4d: Trade discount impact on margin
-- Rows: ~30-40 (on-promo vs off-promo by category)
SELECT
    p.CATEGORY,
    f.IS_ON_PROMOTION,
    COUNT(*) as TRANSACTION_COUNT,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.COGS) as COGS,
    SUM(f.TRADE_DISCOUNT) as TRADE_DISCOUNT,
    ROUND(100.0 * SUM(f.TRADE_DISCOUNT) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as DISCOUNT_RATE_PCT,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS) - SUM(f.TRADE_DISCOUNT)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as NET_MARGIN_AFTER_DISCOUNT_PCT
FROM FACT_POS_SALES f
JOIN DIM_PRODUCT p ON f.PRODUCT_KEY = p.PRODUCT_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY p.CATEGORY, f.IS_ON_PROMOTION
ORDER BY p.CATEGORY, f.IS_ON_PROMOTION DESC;

-- ============================================================
-- 5. CUSTOMER INTELLIGENCE
-- ============================================================
-- KPIs: Customer tier analysis, channel contribution, customer ranking, account scorecards

-- Query 5a: Customer tier analysis
-- Rows: ~4-5 (customer tiers)
SELECT
    c.CUSTOMER_TIER,
    COUNT(DISTINCT c.CUSTOMER_KEY) as CUSTOMER_COUNT,
    SUM(f.NET_REVENUE) as TOTAL_REVENUE,
    ROUND(AVG(f.NET_REVENUE), 0) as AVG_REVENUE_PER_TRANSACTION,
    SUM(CASE WHEN f.IS_ON_PROMOTION = 1 THEN 1 ELSE 0 END) as PROMO_TRANSACTIONS,
    ROUND(100.0 * COUNT(DISTINCT f.CUSTOMER_KEY) / COUNT(DISTINCT f.CUSTOMER_KEY) OVER (), 1) as PCT_OF_CUSTOMERS
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_TIER
ORDER BY TOTAL_REVENUE DESC;

-- Query 5b: Top 20 customers by revenue with channel mix
-- Rows: 20
SELECT
    c.CUSTOMER_NAME,
    c.CUSTOMER_TIER,
    c.CHANNEL,
    c.STORE_COUNT,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    ROUND(100.0 * (SUM(f.NET_REVENUE) - SUM(f.COGS)) / NULLIF(SUM(f.NET_REVENUE), 0), 1) as GROSS_MARGIN_PCT,
    COUNT(DISTINCT f.DATE_KEY) as DAYS_WITH_SALES
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_NAME, c.CUSTOMER_TIER, c.CHANNEL, c.STORE_COUNT
ORDER BY NET_REVENUE DESC
LIMIT 20;

-- Query 5c: Customer concentration and risk
-- Rows: ~5-10
SELECT
    'TOP 5' as SEGMENT,
    COUNT(DISTINCT c.CUSTOMER_KEY) as CUSTOMER_COUNT,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    ROUND(100.0 * SUM(f.NET_REVENUE) / SUM(SUM(f.NET_REVENUE)) OVER (), 1) as PCT_OF_TOTAL_REVENUE
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
  AND c.CUSTOMER_KEY IN (SELECT CUSTOMER_KEY FROM FACT_POS_SALES f2
                         JOIN DIM_DATE d2 ON f2.DATE_KEY = d2.DATE_KEY
                         WHERE d2.FISCAL_YEAR = YEAR(CURRENT_DATE)
                         GROUP BY CUSTOMER_KEY
                         ORDER BY SUM(NET_REVENUE) DESC LIMIT 5)
UNION ALL
SELECT
    'ALL OTHER',
    COUNT(DISTINCT c.CUSTOMER_KEY),
    SUM(f.NET_REVENUE),
    ROUND(100.0 * SUM(f.NET_REVENUE) / SUM(SUM(f.NET_REVENUE)) OVER (), 1)
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
  AND c.CUSTOMER_KEY NOT IN (SELECT CUSTOMER_KEY FROM FACT_POS_SALES f2
                             JOIN DIM_DATE d2 ON f2.DATE_KEY = d2.DATE_KEY
                             WHERE d2.FISCAL_YEAR = YEAR(CURRENT_DATE)
                             GROUP BY CUSTOMER_KEY
                             ORDER BY SUM(NET_REVENUE) DESC LIMIT 5);

-- Query 5d: Customer loyalty and purchase frequency
-- Rows: ~4-5 (purchase frequency buckets)
SELECT
    c.CUSTOMER_TIER,
    CASE
        WHEN COUNT(DISTINCT f.DATE_KEY) >= 300 THEN 'DAILY'
        WHEN COUNT(DISTINCT f.DATE_KEY) >= 100 THEN 'WEEKLY'
        WHEN COUNT(DISTINCT f.DATE_KEY) >= 20 THEN 'MONTHLY'
        ELSE 'OCCASIONAL'
    END as PURCHASE_FREQUENCY,
    COUNT(DISTINCT c.CUSTOMER_KEY) as CUSTOMER_COUNT,
    SUM(f.NET_REVENUE) as TOTAL_REVENUE,
    ROUND(AVG(f.NET_REVENUE), 0) as AVG_TRANSACTION_VALUE
FROM FACT_POS_SALES f
JOIN DIM_CUSTOMER c ON f.CUSTOMER_KEY = c.CUSTOMER_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY c.CUSTOMER_TIER, CASE
        WHEN COUNT(DISTINCT f.DATE_KEY) >= 300 THEN 'DAILY'
        WHEN COUNT(DISTINCT f.DATE_KEY) >= 100 THEN 'WEEKLY'
        WHEN COUNT(DISTINCT f.DATE_KEY) >= 20 THEN 'MONTHLY'
        ELSE 'OCCASIONAL'
    END
ORDER BY c.CUSTOMER_TIER, TOTAL_REVENUE DESC;

-- ============================================================
-- 6. CHANNEL & POS ANALYTICS
-- ============================================================
-- KPIs: Channel revenue mix, channel growth trend, geography × channel

-- Query 6a: Channel revenue with growth trend
-- Rows: ~24 (12 months × 2 channels avg)
SELECT
    d.MONTH_NUM,
    d.MONTH_NAME,
    d.YEAR_NUM,
    ch.CHANNEL_NAME,
    ch.CHANNEL_GROUP,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    SUM(f.UNITS_SOLD) as UNITS_SOLD,
    ROUND(AVG(f.BASE_PRICE), 2) as AVG_PRICE,
    LAG(SUM(f.NET_REVENUE)) OVER (PARTITION BY ch.CHANNEL_NAME ORDER BY d.YEAR_NUM, d.MONTH_NUM) as PRIOR_MONTH_REVENUE
FROM FACT_POS_SALES f
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
JOIN DIM_CHANNEL ch ON f.CHANNEL_KEY = ch.CHANNEL_KEY
WHERE d.YEAR_NUM >= YEAR(CURRENT_DATE) - 1
GROUP BY d.MONTH_NUM, d.MONTH_NAME, d.YEAR_NUM, ch.CHANNEL_NAME, ch.CHANNEL_GROUP
ORDER BY d.YEAR_NUM DESC, d.MONTH_NUM, ch.CHANNEL_NAME;

-- Query 6b: Geographic penetration by channel
-- Rows: ~40-50 (states × channels)
SELECT
    g.REGION,
    g.STATE_PROVINCE,
    g.MARKET_TIER,
    ch.CHANNEL_NAME,
    SUM(f.NET_REVENUE) as NET_REVENUE,
    COUNT(DISTINCT f.CUSTOMER_KEY) as CUSTOMER_COUNT,
    COUNT(DISTINCT f.PRODUCT_KEY) as SKU_COUNT
FROM FACT_POS_SALES f
JOIN DIM_GEOGRAPHY g ON f.GEO_KEY = g.GEO_KEY
JOIN DIM_CHANNEL ch ON f.CHANNEL_KEY = ch.CHANNEL_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY g.REGION, g.STATE_PROVINCE, g.MARKET_TIER, ch.CHANNEL_NAME
ORDER BY NET_REVENUE DESC
LIMIT 50;

