JOIN DIM_HOUSEHOLD h ON f.HOUSEHOLD_KEY = h.HOUSEHOLD_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY h.LOYALTY_TIER
ORDER BY HOUSEHOLD_COUNT DESC;

-- Query 10b: Household income and purchase behavior
-- Rows: ~6-8 (income bands)
SELECT
    h.INCOME_BAND,
    h.PANEL_SEGMENT,
    COUNT(DISTINCT h.HOUSEHOLD_KEY) as HOUSEHOLD_COUNT,
    SUM(f.UNITS_PURCHASED) as TOTAL_UNITS,
    SUM(f.AMOUNT_PAID) as TOTAL_REVENUE,
    ROUND(AVG(f.AMOUNT_PAID), 2) as AVG_BASKET_SIZE,
    ROUND(100.0 * SUM(CASE WHEN f.IS_REPEAT_PURCHASE = 1 THEN 1 ELSE 0 END) / NULLIF(COUNT(*), 0), 1) as REPEAT_RATE_PCT
FROM FACT_HOUSEHOLD_PURCHASE f
JOIN DIM_HOUSEHOLD h ON f.HOUSEHOLD_KEY = h.HOUSEHOLD_KEY
JOIN DIM_DATE d ON f.DATE_KEY = d.DATE_KEY
WHERE d.FISCAL_YEAR = YEAR(CURRENT_DATE)
GROUP BY h.INCOME_BAND, h.PANEL_SEGMENT
ORDER BY TOTAL_REVENUE DESC;

-- Query 10c: Brand loyalty by household segment
-- Rows: ~40-50 (segments × brands)
SELECT
    h.PANEL_SEGMENT,
    p.BRAND,
    COUNT(DISTINCT h.HOUSEHOLD_KEY) as HH_PENETRATION,
    SUM(f.UNITS_PURCHASED) as UNITS,
    SUM(f.AMOUNT_PAID) as REVENUE,
    ROUND(100.0 * SUM(CASE WHEN f.IS_REPEAT_PURCHASE = 1 THEN 1 ELSE 0 END) / NULLIF(COUNT(*), 0), 1) as REPEAT_RATE_PCT,
    COUNT(DISTINCT f.DATE_KEY) as PURCHASE_OCCASIONS
