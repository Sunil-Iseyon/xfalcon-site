-- 1.1: Total Headcount (Current, by Status)
SELECT
  COUNT(DISTINCT EMPLOYEE_ID) AS TOTAL_HEADCOUNT,
  COUNT(DISTINCT CASE WHEN EMPLOYEE_STATUS = 'Active' THEN EMPLOYEE_ID END) AS ACTIVE,
  COUNT(DISTINCT CASE WHEN EMPLOYEE_STATUS = 'On Leave' THEN EMPLOYEE_ID END) AS ON_LEAVE,
  COUNT(DISTINCT CASE WHEN EMPLOYEE_STATUS = 'Terminated' THEN EMPLOYEE_ID END) AS TERMINATED
FROM EMPLOYEES
WHERE EMPLOYEE_STATUS IN ('Active', 'On Leave');

-- 1.2: Headcount by Department
SELECT
  D.DEPARTMENT_NAME,
  D.DEPARTMENT_CODE,
  COUNT(DISTINCT E.EMPLOYEE_ID) AS HEADCOUNT
FROM EMPLOYEES E
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON E.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.END_DATE IS NULL
JOIN DEPARTMENTS D
  ON EA.DEPARTMENT_ID = D.DEPARTMENT_ID
WHERE E.EMPLOYEE_STATUS IN ('Active', 'On Leave')
  AND D.IS_ACTIVE = TRUE
GROUP BY D.DEPARTMENT_ID, D.DEPARTMENT_NAME, D.DEPARTMENT_CODE
ORDER BY HEADCOUNT DESC;

-- 1.3: Turnover Rate (Annual, All Types)
SELECT
  ROUND(
    (COUNT(DISTINCT E.EMPLOYEE_ID) / AVG_HC) * 100,
    2
  ) AS TURNOVER_RATE_PCT
FROM (
  SELECT COUNT(DISTINCT EMPLOYEE_ID) AS CNT
  FROM EXIT_RECORDS
  WHERE TERMINATION_DATE >= DATE_TRUNC('year', CURRENT_DATE)
) E
CROSS JOIN (
  SELECT AVG(HC) AS AVG_HC FROM (
    SELECT
      DATE_TRUNC('month', EFFECTIVE_DATE) AS MONTH,
      COUNT(DISTINCT EMPLOYEE_ID) AS HC
    FROM EMPLOYEE_ASSIGNMENTS
    WHERE END_DATE IS NULL
    GROUP BY DATE_TRUNC('month', EFFECTIVE_DATE)
  ) MONTHLY_HC
) HC;

-- 1.4: New Hires YTD
SELECT
  COUNT(DISTINCT EMPLOYEE_ID) AS NEW_HIRES_YTD,
  ROUND(AVG(YEAR(HIRE_DATE)), 0) AS AVG_HIRE_YEAR
FROM EMPLOYEES
WHERE YEAR(HIRE_DATE) = YEAR(CURRENT_DATE)
  AND EMPLOYEE_STATUS IN ('Active', 'On Leave');

