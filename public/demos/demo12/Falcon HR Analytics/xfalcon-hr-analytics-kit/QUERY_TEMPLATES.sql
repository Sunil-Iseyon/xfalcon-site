-- 2.1: Headcount by Department with Cost Center
SELECT
  D.DEPARTMENT_NAME,
  CC.COST_CENTER_CODE,
  CC.COST_CENTER_NAME,
  COUNT(DISTINCT E.EMPLOYEE_ID) AS HEADCOUNT,
  COUNT(DISTINCT CASE WHEN E.EMPLOYMENT_TYPE = 'Full-time' THEN E.EMPLOYEE_ID END) AS FTE_FULL_TIME,
  COUNT(DISTINCT CASE WHEN E.EMPLOYMENT_TYPE = 'Part-time' THEN E.EMPLOYEE_ID END) AS FTE_PART_TIME,
  COUNT(DISTINCT CASE WHEN E.EMPLOYMENT_TYPE = 'Contract' THEN E.EMPLOYEE_ID END) AS FTE_CONTRACT
FROM EMPLOYEES E
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON E.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.END_DATE IS NULL
JOIN DEPARTMENTS D
  ON EA.DEPARTMENT_ID = D.DEPARTMENT_ID
LEFT JOIN COST_CENTERS CC
  ON D.COST_CENTER_ID = CC.COST_CENTER_ID
WHERE E.EMPLOYEE_STATUS IN ('Active', 'On Leave')
  AND D.IS_ACTIVE = TRUE
GROUP BY D.DEPARTMENT_ID, D.DEPARTMENT_NAME, CC.COST_CENTER_ID, CC.COST_CENTER_CODE, CC.COST_CENTER_NAME
ORDER BY HEADCOUNT DESC;

-- 2.2: Department Turnover Rate (Last 12 Months)
SELECT
  D.DEPARTMENT_NAME,
  COUNT(DISTINCT ER.EMPLOYEE_ID) AS SEPARATIONS,
  ROUND(
    (COUNT(DISTINCT ER.EMPLOYEE_ID) / NULLIF(AVG_HC, 0)) * 100,
    2
  ) AS TURNOVER_RATE_PCT
FROM EXIT_RECORDS ER
JOIN EMPLOYEES E
  ON ER.EMPLOYEE_ID = E.EMPLOYEE_ID
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON E.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.EFFECTIVE_DATE <= ER.TERMINATION_DATE
  AND (EA.END_DATE IS NULL OR EA.END_DATE >= ER.TERMINATION_DATE)
JOIN DEPARTMENTS D
  ON EA.DEPARTMENT_ID = D.DEPARTMENT_ID
CROSS JOIN (
  SELECT AVG(HC) AS AVG_HC FROM (
    SELECT COUNT(DISTINCT EMPLOYEE_ID) AS HC
    FROM EMPLOYEES
    WHERE EMPLOYEE_STATUS IN ('Active', 'On Leave')
  )
)
WHERE ER.TERMINATION_DATE >= DATE_TRUNC('year', CURRENT_DATE)
GROUP BY D.DEPARTMENT_ID, D.DEPARTMENT_NAME
ORDER BY TURNOVER_RATE_PCT DESC;

