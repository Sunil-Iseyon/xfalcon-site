-- 3.1: Average Base Salary by Job Grade
SELECT
  JG.GRADE_CODE,
  JG.GRADE_NAME,
  COUNT(DISTINCT C.EMPLOYEE_ID) AS EMPLOYEE_COUNT,
  ROUND(AVG(C.AMOUNT), 2) AS AVG_BASE_SALARY,
  ROUND(MIN(C.AMOUNT), 2) AS MIN_SALARY,
  ROUND(MAX(C.AMOUNT), 2) AS MAX_SALARY,
  JG.PAY_RANGE_MIN,
  JG.PAY_RANGE_MID,
  JG.PAY_RANGE_MAX
FROM COMPENSATION_RECORDS C
JOIN EMPLOYEES E
  ON C.EMPLOYEE_ID = E.EMPLOYEE_ID
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON C.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.END_DATE IS NULL
JOIN JOB_GRADES JG
  ON EA.JOB_GRADE_ID = JG.JOB_GRADE_ID
WHERE C.END_DATE IS NULL
  AND C.COMP_TYPE = 'Base'
  AND E.EMPLOYEE_STATUS IN ('Active', 'On Leave')
GROUP BY JG.JOB_GRADE_ID, JG.GRADE_CODE, JG.GRADE_NAME, JG.PAY_RANGE_MIN, JG.PAY_RANGE_MID, JG.PAY_RANGE_MAX
ORDER BY JG.GRADE_CODE;

-- 3.2: Total Compensation Cost Summary
SELECT
  COUNT(DISTINCT C.EMPLOYEE_ID) AS EMPLOYEE_COUNT,
  SUM(C.AMOUNT) AS TOTAL_COMPENSATION,
  ROUND(SUM(C.AMOUNT) / COUNT(DISTINCT C.EMPLOYEE_ID), 2) AS AVG_TOTAL_COMP,
  ROUND(
    SUM(CASE WHEN COMP_TYPE = 'Base' THEN AMOUNT ELSE 0 END) /
    COUNT(DISTINCT CASE WHEN COMP_TYPE = 'Base' THEN EMPLOYEE_ID END),
    2
  ) AS AVG_BASE,
  ROUND(
    SUM(CASE WHEN COMP_TYPE = 'Bonus' THEN AMOUNT ELSE 0 END) /
    COUNT(DISTINCT CASE WHEN COMP_TYPE = 'Bonus' THEN EMPLOYEE_ID END),
    2
  ) AS AVG_BONUS
FROM COMPENSATION_RECORDS C
WHERE C.END_DATE IS NULL;

-- 3.3: Compa-Ratio Distribution by Grade
SELECT
  JG.GRADE_CODE,
  JG.GRADE_NAME,
  ROUND(
    (C.AMOUNT / JG.PAY_RANGE_MID) * 100,
    1
  ) AS COMPA_RATIO,
  COUNT(*) AS EMPLOYEE_COUNT
FROM COMPENSATION_RECORDS C
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON C.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.END_DATE IS NULL
JOIN JOB_GRADES JG
  ON EA.JOB_GRADE_ID = JG.JOB_GRADE_ID
WHERE C.END_DATE IS NULL
  AND C.COMP_TYPE = 'Base'
GROUP BY JG.JOB_GRADE_ID, JG.GRADE_CODE, JG.GRADE_NAME, C.AMOUNT, JG.PAY_RANGE_MID
ORDER BY JG.GRADE_CODE, COMPA_RATIO;

-- =========================================================================
-- DASHBOARD 4: PAY EQUITY ANALYSIS
-- =========================================================================

-- 4.1: Gender Pay Gap by Job Grade
SELECT
  JG.GRADE_CODE,
  JG.GRADE_NAME,
  ROUND(AVG(CASE WHEN E.GENDER = 'M' THEN C.AMOUNT END), 2) AS AVG_MALE_SALARY,
  ROUND(AVG(CASE WHEN E.GENDER = 'F' THEN C.AMOUNT END), 2) AS AVG_FEMALE_SALARY,
  COUNT(DISTINCT CASE WHEN E.GENDER = 'M' THEN C.EMPLOYEE_ID END) AS MALE_COUNT,
  COUNT(DISTINCT CASE WHEN E.GENDER = 'F' THEN C.EMPLOYEE_ID END) AS FEMALE_COUNT,
  ROUND(
    ((AVG(CASE WHEN E.GENDER = 'M' THEN C.AMOUNT END) -
      AVG(CASE WHEN E.GENDER = 'F' THEN C.AMOUNT END)) /
     AVG(CASE WHEN E.GENDER = 'F' THEN C.AMOUNT END)) * 100,
    2
  ) AS PAY_GAP_PCT
FROM COMPENSATION_RECORDS C
JOIN EMPLOYEES E
  ON C.EMPLOYEE_ID = E.EMPLOYEE_ID
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON C.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.END_DATE IS NULL
JOIN JOB_GRADES JG
  ON EA.JOB_GRADE_ID = JG.JOB_GRADE_ID
WHERE C.END_DATE IS NULL
  AND C.COMP_TYPE = 'Base'
  AND E.GENDER IN ('M', 'F')
  AND E.EMPLOYEE_STATUS IN ('Active', 'On Leave')
GROUP BY JG.JOB_GRADE_ID, JG.GRADE_CODE, JG.GRADE_NAME
ORDER BY JG.GRADE_CODE;

-- 4.2: Diversity Index by Ethnicity
SELECT
  ETHNICITY,
  COUNT(DISTINCT EMPLOYEE_ID) AS EMPLOYEE_COUNT,
  ROUND(
    (COUNT(DISTINCT EMPLOYEE_ID) / SUM(COUNT(DISTINCT EMPLOYEE_ID)) OVER()) * 100,
    2
  ) AS PERCENTAGE
FROM EMPLOYEES
WHERE EMPLOYEE_STATUS IN ('Active', 'On Leave')
GROUP BY ETHNICITY
ORDER BY EMPLOYEE_COUNT DESC;

-- =========================================================================
-- DASHBOARD 5: PERFORMANCE MANAGEMENT
-- =========================================================================

-- 5.1: Performance Rating Distribution (Current Cycle)
SELECT
  RATING_LABEL,
  COUNT(DISTINCT REVIEW_ID) AS REVIEW_COUNT,
  COUNT(DISTINCT EMPLOYEE_ID) AS EMPLOYEE_COUNT,
  ROUND(
    (COUNT(DISTINCT EMPLOYEE_ID) / SUM(COUNT(DISTINCT EMPLOYEE_ID)) OVER()) * 100,
    2
  ) AS PERCENTAGE
FROM PERFORMANCE_REVIEWS
WHERE IS_FINAL_CALIBRATED = TRUE
  AND CYCLE_ID IN (
    SELECT CYCLE_ID FROM PERFORMANCE_CYCLES
    WHERE END_DATE >= DATE_TRUNC('year', CURRENT_DATE)
  )
GROUP BY RATING_LABEL
ORDER BY CASE RATING_LABEL
  WHEN 'Exceeds' THEN 1
  WHEN 'Meets' THEN 2
  WHEN 'Below' THEN 3
  ELSE 4
END;

-- 5.2: Average Goal Attainment by Department
SELECT
  D.DEPARTMENT_NAME,
  ROUND(AVG(G.ATTAINMENT_PCT), 2) AS AVG_GOAL_ATTAINMENT_PCT,
  COUNT(DISTINCT G.GOAL_ID) AS GOAL_COUNT,
  COUNT(DISTINCT G.EMPLOYEE_ID) AS EMPLOYEE_COUNT
FROM GOALS G
JOIN EMPLOYEES E
  ON G.EMPLOYEE_ID = E.EMPLOYEE_ID
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON E.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.END_DATE IS NULL
JOIN DEPARTMENTS D
  ON EA.DEPARTMENT_ID = D.DEPARTMENT_ID
WHERE G.CYCLE_ID IN (
  SELECT CYCLE_ID FROM PERFORMANCE_CYCLES
  WHERE END_DATE >= DATE_TRUNC('year', CURRENT_DATE)
)
  AND E.EMPLOYEE_STATUS IN ('Active', 'On Leave')
GROUP BY D.DEPARTMENT_ID, D.DEPARTMENT_NAME
ORDER BY AVG_GOAL_ATTAINMENT_PCT DESC;

-- 5.3: Average Performance Rating by Department
SELECT
  D.DEPARTMENT_NAME,
  ROUND(AVG(PR.OVERALL_RATING), 2) AS AVG_RATING,
  COUNT(DISTINCT PR.EMPLOYEE_ID) AS EMPLOYEE_COUNT,
  COUNT(DISTINCT PR.REVIEW_ID) AS REVIEW_COUNT
FROM PERFORMANCE_REVIEWS PR
JOIN EMPLOYEES E
  ON PR.EMPLOYEE_ID = E.EMPLOYEE_ID
JOIN EMPLOYEE_ASSIGNMENTS EA
  ON E.EMPLOYEE_ID = EA.EMPLOYEE_ID
  AND EA.END_DATE IS NULL
JOIN DEPARTMENTS D
  ON EA.DEPARTMENT_ID = D.DEPARTMENT_ID
WHERE PR.IS_FINAL_CALIBRATED = TRUE
  AND PR.CYCLE_ID IN (
    SELECT CYCLE_ID FROM PERFORMANCE_CYCLES
    WHERE END_DATE >= DATE_TRUNC('year', CURRENT_DATE)
  )
  AND E.EMPLOYEE_STATUS IN ('Active', 'On Leave')
GROUP BY D.DEPARTMENT_ID, D.DEPARTMENT_NAME
ORDER BY AVG_RATING DESC;

